package PracticeDiary_1;

/*
1.格式：
switch(表达式){ 相当于对这个表达式进行判断
case const1:  如果它是const1 那么...
    语句1;
    //break
case const2:  如果它是const2 那么...
    语句2;
    //break
...
default: 相当于else  如果它啥也不是 那么按照默认输出
    语句n;
    //
}
 */
public class code_5_switchCase {
    public static void main(String[] args) {
        int age = 18;
        switch (age) {
            case 17, 14, 15, 16 -> System.out.println("Nope");
            default -> System.out.println("Yep");
        }
    }
}
